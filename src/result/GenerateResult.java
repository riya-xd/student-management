    private void roll_txtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roll_txtActionPerformed
        // TODO add your handling code here:
        try {
        Connection con = DBConnect.connect();
        PreparedStatement ps = con.prepareStatement("select * from result where rollno = ?");
        ps.setString(1, roll_txt.getSelectedItem().toString());
        
        ResultSet rs = ps.executeQuery();
        
        if (rs.next()) {
            String json = rs.getString("sub"); //[]

            // Convert JSON string back to ArrayList of Pairs
            Gson gson = new Gson(); 
            java.lang.reflect.Type listType = new TypeToken<ArrayList<Pair<String, Integer>>>
        (){}.getType();
            ArrayList<Pair<String, Integer>> p1 = gson.fromJson(json, listType);

            // Remove all existing components from result_pnl
            result_pnl.removeAll();

            // Use p1 as needed
            
            x = 0;
            for (Pair<String, Integer> pair : p1) {
                x++;
                javax.swing.JLabel newj = new javax.swing.JLabel();
//                newj.setFont(new java.awt.Font("Segoe UI", 0, 18)); // NOI18N
//                newj.setForeground(new java.awt.Color(0, 0, 102));
                newj.setOpaque(true);
                newj.setText(pair.getKey() + "                                               " + pair.getValue());
                result_pnl.add(newj);
            }
            
            // Update result_pnl's layout
            result_pnl.setLayout(new java.awt.GridLayout(x, 2, 20, 20));
            result_pnl.revalidate();
            result_pnl.repaint();
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_roll_txtActionPerformed

