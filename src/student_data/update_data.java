    private void submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_btnActionPerformed
        // TODO add your handling code here:
        if(changeCourse==1){
                   course = course_txt.getSelectedItem().toString();
               }
               if(changeBranch==1){
                   branch = branch_txt.getSelectedItem().toString();
               }
         try{
            Connection con = DBConnect.connect();
            PreparedStatement ps = con.prepareStatement("update student set name=?,rollno=?,"+
                    "email=?,xii=?,course=?,aadhar=?,"+
                    "address=?,fname=?,dob=?,phone=?,x=?,branch=?,image=? where rollno=?");
            ps.setString(1,name_txt.getText()); // name
            ps.setString(2,rno_txt.getText()); // roll no
            ps.setString(3,email_txt.getText()); // email id
            ps.setString(4,xii_txt.getText()); // class xii
            ps.setString(5,course); // course
            ps.setString(6,adhar_txt.getText()); // aadhar
            ps.setString(7,address_txt.getText()); // address
            ps.setString(8,fname_txt.getText()); // father
            ps.setString(9,dob_txt.getText()); // dob
            ps.setString(10,phone_txt.getText()); // phone
            ps.setString(11,x_txt.getText()); // x class
            ps.setString(12,branch); // branch
            if(changeImage==1){
                InputStream ig = new FileInputStream(new File(imgPath));
                ps.setBlob(13,ig);
            }
            else{
                ps.setBlob(13,new ByteArrayInputStream(oldImage));
            }
            ps.setString(14,rno_txt.getText());
            int a = ps.executeUpdate();
            if(a==1){
                JOptionPane.showMessageDialog(null,"Successfully update a new Student");
                dispose();
            }
            else
            JOptionPane.showMessageDialog(null,"Some Error");

        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_submit_btnActionPerformed

