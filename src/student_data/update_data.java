    private void search_cbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_cbActionPerformed
        // TODO add your handling code here:
        try{
            Connection con = DBConnect.connect();
            PreparedStatement ps = con.prepareStatement("select * from student where rollno=?");
            ps.setString(1,search_cb.getSelectedItem().toString()); 

            ResultSet rs = ps.executeQuery();
           while(rs.next()){
               name_txt.setText(rs.getString(1));// name
               rno_txt.setText(rs.getString(2));// rollno
               email_txt.setText(rs.getString(3));// email
               xii_txt.setText(rs.getString(4));// xii
                // course
                course = rs.getString(5);
               adhar_txt.setText(rs.getString(6));// adhar
               address_txt.setText(rs.getString(7));// address
               fname_txt.setText(rs.getString(8));// fname
               dob_txt.setText(rs.getString(9));// dob
               phone_txt.setText(rs.getString(10));// phone
               x_txt.setText(rs.getString(11));// x
                // branch
                branch = rs.getString(12);
                // image
                oldImage =rs.getBytes(13);
                img.setIcon(resizePic(oldImage));
               
           }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_search_cbActionPerformed

