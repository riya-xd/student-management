    public ImageIcon resizePic(String pic){
        ImageIcon myimg = new ImageIcon(pic);
        Image img1 = myimg.getImage().getScaledInstance(img.getWidth(),img.getHeight(),Image.SCALE_SMOOTH);
        return new ImageIcon(img1);
    }
    public void generateQR(){
        Map<String,String> data = new HashMap<>();
        data.put("name",name_txt.getText());
        data.put("email",email_txt.getText());
        data.put("rollno",rno_txt.getText());
        Gson gson = new Gson();
        String jsonData = gson.toJson(data);
        out = QRCode.from(jsonData).withSize(300,250).to(ImageType.PNG).stream();
        try{
            imageData = out.toByteArray();
            icon = new ImageIcon(imageData); 
        }catch(Exception e){}
    }
