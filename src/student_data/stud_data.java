    private void upload_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upload_btnActionPerformed
        // TODO add your handling code here:
        //to generate pop-up box that will allow to select a file
        JFileChooser file = new JFileChooser();
        //to set the first folder to appear 
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        //to select the suitable file type 
        FileNameExtensionFilter filefilter = new FileNameExtensionFilter(".Images","jpg","png","jpeg","gif");
        //to add the suitable file extension to the JFileChooser object named file
        file.addChoosableFileFilter(filefilter);
        //to get thje state if user have selected a file or not
        //if an image is selected then filestate = 1 otherwise filstate = 0 
        int filestate = file.showSaveDialog(null);
        //conditional Statements
        if (filestate == JFileChooser.APPROVE_OPTION){
         //if an image is selected then get the path of folder
            File sf = file.getSelectedFile();
            //get the absolute path i.e starting form the drive eg: C:/Downloads...... the complete path
            String path = sf.getAbsolutePath();
            //send the image/PATH TO RESIZEpIC METHOD TO ADJJUST THE SIZE OF IMAGE SELECTED ACCORDING to the label
            //then set the modified image into label
            img.setIcon(resizePic(path));
            imgPath = path;
        }
        else if(filestate == JFileChooser.CANCEL_OPTION)
            JOptionPane.showConfirmDialog(null,"NO IMAGE SELECTED");    
    }//GEN-LAST:event_upload_btnActionPerformed

